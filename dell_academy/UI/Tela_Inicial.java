    private void btnApostarActionPerformed(java.awt.event.ActionEvent evt) {
       
        Pessoa p = new Pessoa(JComboBox.getSelectedItem());
        // Inicializa um array de inteiros para armazenar os nmeros apostados
        int[] numerosApostados = new int[5];
    
        // Preenche o array com os nmeros selecionados nos botes
        // Adicione os demais botes conforme necessrio
    
        // Cria uma nova aposta com os nmeros selecionados e o apostador associado
        Aposta aposta = new Aposta(operador.listarPessoas(p), numerosApostados);
    
        // Adiciona a nova aposta  lista de apostas feitas pela pessoa
        pessoa.adicionarAposta(aposta);
    
        // Exibe os nmeros apostados pela pessoa
        JOptionPane.showMessageDialog(null, aposta.getNumerosApostados(pessoa));
    }

